/* ─── APPLY LANGUAGE ─── */
function applyLang(lang) {
  const t = i18n[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] && typeof t[key] === 'string') el.textContent = t[key];
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  renderTerminal(lang);
}

/* ─── TERMINAL ─── */
function renderTerminal(lang) {
  const lines = i18n[lang].terminal;
  const body = document.getElementById('terminal-body');
  body.innerHTML = '';

  lines.forEach(line => {
    const span = document.createElement('span');
    span.className = 't-line';
    if (line.startsWith('$')) {
      span.innerHTML = `<span class="t-prompt">$</span><span class="t-cmd">${line.slice(1)}</span>`;
    } else if (line.startsWith('#')) {
      span.innerHTML = `<span class="t-comment">${line}</span>`;
    } else {
      span.innerHTML = `<span class="t-out">${line}</span>`;
    }
    body.appendChild(span);
  });

  const cursor = document.createElement('span');
  cursor.className = 't-line';
  cursor.innerHTML = `<span class="t-prompt">$</span><span class="t-cursor"></span>`;
  body.appendChild(cursor);
}

/* ─── LANGUAGE BUTTONS ─── */
document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

/* ─── HAMBURGER ─── */
const hamburger = document.getElementById('hamburger');
const navLinks = document.getElementById('nav-links');

hamburger.addEventListener('click', () => navLinks.classList.toggle('open'));
navLinks.querySelectorAll('a').forEach(a => {
  a.addEventListener('click', () => navLinks.classList.remove('open'));
});

/* ─── REVEAL ON SCROLL ─── */
const observer = new IntersectionObserver(entries => {
  entries.forEach(e => {
    if (e.isIntersecting) e.target.classList.add('visible');
  });
}, { threshold: 0.1 });

document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

/* ─── INIT ─── */
applyLang('en');
