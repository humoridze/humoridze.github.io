var currentLang = 'en';
var currentTheme = 'dark';
var langSwitchTimer = null;

function applyLang(lang, animated) {
  if (lang === currentLang && animated) return;
  var t = T[lang];
  var els = Array.from(document.querySelectorAll('[data-i18n]:not([data-no-lang-anim])'));
  var allI18n = Array.from(document.querySelectorAll('[data-i18n]'));

  if (!animated) {
    /* silent first render */
    currentLang = lang;
    allI18n.forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });
    document.querySelectorAll('[data-lang-btn]').forEach(function(btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang-btn') === lang);
    });
    renderTerminal(lang);
    document.documentElement.lang = lang;
    return;
  }

  /* cancel any pending switch */
  if (langSwitchTimer) { clearTimeout(langSwitchTimer); langSwitchTimer = null; }

  /* step 1 — fade out */
  els.forEach(function(el) {
    el.classList.remove('lang-in');
    el.classList.add('lang-out');
  });

  langSwitchTimer = setTimeout(function() {
    langSwitchTimer = null;
    /* step 2 — swap text for ALL i18n elements */
    currentLang = lang;
    allI18n.forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });
    els.forEach(function(el) {
      el.classList.remove('lang-out');
    });
    document.querySelectorAll('[data-lang-btn]').forEach(function(btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang-btn') === lang);
    });
    renderTerminal(lang);
    document.documentElement.lang = lang;

    /* step 3 — staggered fade in */
    els.forEach(function(el, i) {
      el.classList.add('lang-in');
      el.style.animationDelay = Math.min(i * 0.018, 0.18) + 's';
    });

    /* cleanup */
    setTimeout(function() {
      els.forEach(function(el) {
        el.classList.remove('lang-in');
        el.style.animationDelay = '';
      });
    }, 500);

  }, 170);
}

